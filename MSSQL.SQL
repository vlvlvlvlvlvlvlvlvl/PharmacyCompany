CREATE DATABASE PharmacyCompany;

CREATE TABLE Product
(
Id INT NOT NULL IDENTITY(1,1) PRIMARY KEY,
Title VARCHAR(100)
);

CREATE TABLE Pharmacy
(
Id INT NOT NULL IDENTITY(1,1) PRIMARY KEY,
Title VARCHAR(100),
Address VARCHAR(300),
Phone VARCHAR(20)
);

CREATE TABLE Warehouse
(
Id INT NOT NULL IDENTITY(1,1) PRIMARY KEY,
Title VARCHAR(100),
PharmacyId INT FOREIGN KEY REFERENCES Pharmacy(id) ON DELETE CASCADE
);

CREATE TABLE Consignment
(
Id INT NOT NULL IDENTITY(1,1) PRIMARY KEY,
WarehouseId INT FOREIGN KEY REFERENCES Warehouse(id) ON DELETE CASCADE,
ProductId INT FOREIGN KEY REFERENCES Product(id) ON DELETE CASCADE,
Quantity INT
);